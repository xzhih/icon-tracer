fn annular_sector_template_point(
    center: (f64, f64),
    outer_radius: f64,
    angle: f64,
    point: (f64, f64),
) -> (f64, f64) {
    let cos = angle.cos();
    let sin = angle.sin();
    (
        center.0 + outer_radius * (point.0 * cos - point.1 * sin),
        center.1 + outer_radius * (point.0 * sin + point.1 * cos),
    )
}
