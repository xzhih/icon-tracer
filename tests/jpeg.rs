use icon_tracer::{Bitmap, RasterError, RasterOptions, RgbaImage};

#[test]
fn parses_jpeg_file_by_magic_bytes() {
    let bitmap =
        Bitmap::from_bytes(jpeg_1x1_black(), RasterOptions::default()).expect("JPEG should parse");

    assert_eq!(bitmap.width(), 1);
    assert_eq!(bitmap.height(), 1);
    assert!(bitmap.is_black(0, 0));
}

#[test]
fn malformed_jpeg_reports_jpeg_error() {
    let error = Bitmap::from_bytes(b"\xff\xd8not a complete jpeg", RasterOptions::default())
        .expect_err("malformed JPEG should fail");

    assert!(matches!(error, RasterError::Jpeg(_)));
}

#[test]
fn raw_jpeg_decode_preserves_rgb_with_opaque_alpha() {
    let image = RgbaImage::from_jpeg(jpeg_1x1_color()).expect("JPEG should parse as raw RGBA");

    assert_eq!(image.width(), 1);
    assert_eq!(image.height(), 1);
    let pixel = image.pixel(0, 0);
    assert!((190..=220).contains(&pixel.red), "{pixel:?}");
    assert!((40..=80).contains(&pixel.green), "{pixel:?}");
    assert!((110..=145).contains(&pixel.blue), "{pixel:?}");
    assert_eq!(pixel.alpha, 255);
}

